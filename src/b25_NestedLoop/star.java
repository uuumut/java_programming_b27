package b25_NestedLoop;

public class star {
    public static void main(String[] args) {
        String star = "* ";
        for (int j = 1; j <9 ; j++) {

            for (int i = 1; i <10 ; i++) {
                System.out.print(star);
            }
            System.out.println();
        }


    }
}
