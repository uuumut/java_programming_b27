package b25_ForLoop;

public class aToZ {
    public static void main(String[] args) {
        for (char c='a';c<='z'; c++){
            System.out.print(c);
        }
        System.out.println("...................");
        for (int i=65; i<=90; i++){
            System.out.print((char)i );
        }
    }
}
